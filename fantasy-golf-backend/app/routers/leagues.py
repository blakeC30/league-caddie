@router.get("/join/{invite_code}", response_model=LeagueJoinPreview)
def preview_join(
    invite_code: str,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    """
    Preview a league before submitting a join request.

    Returns the league name, member count, and the current user's
    relationship to it (None = no request, "pending" = waiting, "approved" = member).
    Does not create or modify any records.
    """
    league = db.query(League).filter_by(invite_code=invite_code).first()
    if not league:
        raise HTTPException(status_code=404, detail="Invalid invite link — league not found")

    member_count = db.query(LeagueMember).filter_by(
        league_id=league.id, status=LeagueMemberStatus.APPROVED.value
    ).count()

    existing = db.query(LeagueMember).filter_by(
        league_id=league.id, user_id=current_user.id
    ).first()

    return LeagueJoinPreview(
        league_id=league.id,
        name=league.name,
        member_count=member_count,
        user_status=existing.status if existing else None,
    )


@router.get("/my-requests", response_model=list[LeagueRequestOut])
def my_join_requests(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    """Return the current user's pending join requests across all leagues."""
    rows = (
        db.query(LeagueMember, League)
        .join(League, LeagueMember.league_id == League.id)
        .filter(
            LeagueMember.user_id == current_user.id,
            LeagueMember.status == LeagueMemberStatus.PENDING.value,
        )
        .all()
    )
    return [
        LeagueRequestOut(
            league_id=league.id,
            league_name=league.name,
            requested_at=member.joined_at,
        )
        for member, league in rows
    ]


@router.post("/join/{invite_code}", response_model=LeagueMemberOut, status_code=201)
@limiter.limit("10/hour")
def request_to_join(
    request: Request,
    invite_code: str,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    """
    Submit a join request using a league's invite code.

    Private leagues (default): creates a "pending" membership. A league manager must
    approve before the user has access to the league.

    Public leagues (future): auto-approves and returns an "approved" membership.

    The invite code is shared by league managers; it is the only way to request
    membership. Admins can find their invite link in the Admin panel.

    Rate-limited to 10 requests per IP per hour to prevent invite-code brute-forcing.
    Max 50 pending requests per league. Max 500 approved members per league.
    """
    league = db.query(League).filter_by(invite_code=invite_code).first()
    if not league:
        raise HTTPException(status_code=404, detail="Invalid invite link — league not found")

    existing = db.query(LeagueMember).filter_by(
        league_id=league.id, user_id=current_user.id
    ).first()
    if existing:
        if existing.status == LeagueMemberStatus.APPROVED.value:
            raise HTTPException(status_code=409, detail="You are already a member of this league")
        raise HTTPException(status_code=409, detail="You already have a pending join request for this league")

    # Guard: per-user league cap — block the request now so the user isn't waiting for
    # an approval that can never succeed.
    user_league_count = db.query(LeagueMember).filter_by(
        user_id=current_user.id,
        status=LeagueMemberStatus.APPROVED.value,
    ).count()
    if user_league_count >= USER_LEAGUE_CAP:
        raise HTTPException(
            status_code=400,
            detail=f"You have reached the maximum of {USER_LEAGUE_CAP} leagues. Leave a league before joining another.",
        )

    # Guard: too many pending requests already in the queue for this league.
    pending_count = db.query(LeagueMember).filter_by(
        league_id=league.id, status=LeagueMemberStatus.PENDING.value
    ).count()
    if pending_count >= LEAGUE_PENDING_CAP:
        raise HTTPException(
            status_code=400,
            detail="This league has too many pending join requests. Please try again later or contact the league manager.",
        )

    # Public leagues skip the approval step — check member cap before auto-approving.
    if league.is_public:
        approved_count = db.query(LeagueMember).filter_by(
            league_id=league.id, status=LeagueMemberStatus.APPROVED.value
        ).count()
        if approved_count >= LEAGUE_MEMBER_CAP:
            raise HTTPException(
                status_code=400,
                detail=f"This league has reached its maximum member limit of {LEAGUE_MEMBER_CAP}.",
            )

    initial_status = (
        LeagueMemberStatus.APPROVED.value
        if league.is_public
        else LeagueMemberStatus.PENDING.value
    )

    membership = LeagueMember(
        league_id=league.id,
        user_id=current_user.id,
        role=LeagueMemberRole.MEMBER.value,
        status=initial_status,
    )
    db.add(membership)
    db.commit()
    db.refresh(membership)
    membership.user = current_user
    return membership


