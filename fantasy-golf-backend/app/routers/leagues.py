def _playoff_tournament_ids_for_league(league_id: uuid.UUID, db: Session) -> frozenset[uuid.UUID]:
    """Return the set of tournament IDs that are playoff rounds for this league.

    Two sources are combined so the result is correct at every stage of the
    playoff lifecycle:

    1. PlayoffRound.tournament_id records — authoritative once the admin opens
       each round (via POST /playoff/rounds/{id}/open). These are only populated
       after the bracket has been initialised.

    2. Computed from PlayoffConfig: the last N scheduled league tournaments
       (excluding the globally-next upcoming, which is the current pick week)
       where N = required rounds for the bracket size. This handles the period
       before any PlayoffRound records exist and serves as a fallback.

    A tournament is marked is_playoff_round=True if it appears in either source.
    Mirrors the savedPlayoffRoundMap logic in the frontend ManageLeague component.
    """
    ids: set[uuid.UUID] = set()

    # ── Source 1: explicit PlayoffRound assignments ──────────────────────────
    explicit = (
        db.query(PlayoffRound.tournament_id)
        .join(PlayoffConfig, PlayoffRound.playoff_config_id == PlayoffConfig.id)
        .filter(
            PlayoffConfig.league_id == league_id,
            PlayoffRound.tournament_id.isnot(None),
        )
        .all()
    )
    ids.update(row.tournament_id for row in explicit)

    # ── Source 2: computed from playoff config ───────────────────────────────
    config = (
        db.query(PlayoffConfig)
        .filter(PlayoffConfig.league_id == league_id)
        .first()
    )
    if config and config.playoff_size >= 2:
        # Mirror _required_rounds() from playoff.py — log2 of bracket size.
        required = 4 if config.playoff_size == 32 else int(math.log2(config.playoff_size))

        # Exclude the globally-next upcoming tournament (reserved for the current
        # pick week) only when no league tournament is currently in progress.
        # While in progress, the next tournament's pick window hasn't opened yet,
        # so it remains eligible as a playoff round.
        has_in_progress = db.query(
            db.query(LeagueTournament)
            .filter_by(league_id=league_id)
            .join(LeagueTournament.tournament)
            .filter(Tournament.status == TournamentStatus.IN_PROGRESS.value)
            .exists()
        ).scalar()

        next_upcoming_id = None
        if not has_in_progress:
            next_upcoming = (
                db.query(Tournament)
                .filter(Tournament.status == TournamentStatus.SCHEDULED.value)
                .order_by(Tournament.start_date.asc())
                .first()
            )
            next_upcoming_id = next_upcoming.id if next_upcoming else None

        candidates_q = (
            db.query(LeagueTournament)
            .filter_by(league_id=league_id)
            .join(LeagueTournament.tournament)
            .filter(Tournament.status == TournamentStatus.SCHEDULED.value)
        )
        if next_upcoming_id is not None:
            candidates_q = candidates_q.filter(Tournament.id != next_upcoming_id)

        # Last N by start_date ascending = the playoff rounds.
        rows = candidates_q.order_by(Tournament.start_date.asc()).all()
        for row in rows[-required:]:
            ids.add(row.tournament.id)

    return frozenset(ids)


def _build_league_tournament_out(
    row: LeagueTournament,
    db: Session,
    playoff_ids: frozenset[uuid.UUID] = frozenset(),
) -> LeagueTournamentOut:
    """Build a LeagueTournamentOut from a LeagueTournament row, resolving effective_multiplier.

    all_r1_teed_off is only computed for in-progress tournaments (checking the DB
    for the last Round 1 tee time). For scheduled or completed tournaments it is
    always False — the value is irrelevant outside the in-progress window.

    is_playoff_round is True when the tournament is assigned to a PlayoffRound for
    this league. Pass playoff_ids from _playoff_tournament_ids_for_league() so the
    check is O(1) without an extra DB hit per row.
    """
    t = row.tournament
    effective = row.multiplier if row.multiplier is not None else t.multiplier
    in_progress = t.status == TournamentStatus.IN_PROGRESS.value
    return LeagueTournamentOut(
        id=t.id,
        pga_tour_id=t.pga_tour_id,
        name=t.name,
        start_date=t.start_date,
        end_date=t.end_date,
        multiplier=t.multiplier,
        purse_usd=t.purse_usd,
        status=t.status,
        is_team_event=t.is_team_event,
        effective_multiplier=effective,
        all_r1_teed_off=_all_r1_teed_off(db, t.id) if in_progress else False,
        is_playoff_round=t.id in playoff_ids,
    )


@router.get("/{league_id}/tournaments", response_model=list[LeagueTournamentOut])
def get_league_tournaments(
    league_and_member: tuple[League, LeagueMember] = Depends(require_league_member),
    db: Session = Depends(get_db),
):
    """
    Return the tournaments selected for this league's schedule, sorted by
    start_date ascending. Each tournament includes effective_multiplier, which
    resolves to the league's per-tournament override or the global default.
    Returns [] if the admin hasn't configured the schedule yet.
    """
    league, _ = league_and_member
    rows = (
        db.query(LeagueTournament)
        .filter_by(league_id=league.id)
        .join(LeagueTournament.tournament)
        .order_by(Tournament.start_date.asc())
        .all()
    )
    playoff_ids = _playoff_tournament_ids_for_league(league.id, db)
    return [_build_league_tournament_out(row, db, playoff_ids) for row in rows]


class TournamentScheduleItem(BaseModel):
    tournament_id: uuid.UUID
    multiplier: float | None = None  # None = inherit from tournament.multiplier

    @field_validator("multiplier")
    @classmethod
    def multiplier_must_be_valid(cls, v: float | None) -> float | None:
        if v is not None and v not in (1.0, 1.5, 2.0):
            raise ValueError("multiplier must be 1.0, 1.5, or 2.0")
        return v


class TournamentScheduleUpdate(BaseModel):
    tournaments: list[TournamentScheduleItem]


@router.put("/{league_id}/tournaments", response_model=list[LeagueTournamentOut])
def update_league_tournaments(
    body: TournamentScheduleUpdate,
    league_and_manager: tuple[League, LeagueMember] = Depends(require_league_manager),
    db: Session = Depends(get_db),
):
    """
    Atomically replace the league's tournament schedule.

    Accepts a full list of {tournament_id, multiplier} objects.
    Multiplier may be null to inherit the tournament's global default.

    Validation:
    - Schedule is locked once the last regular season tournament is completed
      — no further changes are allowed.
    - At most one tournament per ISO calendar week (same week = same ISO year+week).
    - If the league has a pending playoff config, the new schedule must include
      enough future (scheduled) tournaments to fill the bracket. Playoff rounds
      are automatically the last N scheduled tournaments — no explicit flag needed.

    All completed tournaments are re-scored after save.

    Requires league manager.
    """
    league, _ = league_and_manager

    tournament_ids = [item.tournament_id for item in body.tournaments]

    # Verify all requested tournament IDs actually exist; build lookup dict.
    tournament_map: dict = {}
    if tournament_ids:
        rows = db.query(Tournament).filter(Tournament.id.in_(tournament_ids)).all()
        if len(rows) != len(tournament_ids):
            raise HTTPException(status_code=422, detail="One or more tournament IDs not found")
        tournament_map = {t.id: t for t in rows}

    # Rule: all tournaments must fall within the active season's calendar year.
    active_season = db.query(Season).filter_by(league_id=league.id, is_active=True).first()
    if active_season:
        for t in tournament_map.values():
            if t.start_date.year != active_season.year:
                raise HTTPException(
                    status_code=422,
                    detail=(
                        f"{t.name!r} starts in {t.start_date.year}, but the active season is "
                        f"{active_season.year} — only tournaments from that year can be scheduled"
                    ),
                )

    # Rule: schedule locks once the last regular season tournament is completed.
    # Playoff tournaments are excluded — they can still be managed after the regular season ends.
    playoff_ids = _playoff_tournament_ids_for_league(league.id, db)
    q = (
        db.query(Tournament)
        .join(LeagueTournament, LeagueTournament.tournament_id == Tournament.id)
        .filter(LeagueTournament.league_id == league.id)
    )
    if playoff_ids:
        q = q.filter(Tournament.id.notin_(playoff_ids))
    last_regular_season = q.order_by(Tournament.start_date.desc()).first()
    if last_regular_season and last_regular_season.status == TournamentStatus.COMPLETED.value:
        raise HTTPException(
            status_code=422,
            detail="Schedule is locked — the final regular season tournament has already completed",
        )

    # Rule: only one tournament per ISO calendar week.
    week_map: dict[tuple[int, int], str] = {}
    for item in body.tournaments:
        t = tournament_map.get(item.tournament_id)
        if t:
            iso = t.start_date.isocalendar()
            key = (iso.year, iso.week)
            if key in week_map:
                raise HTTPException(
                    status_code=422,
                    detail=(
                        f"Schedule cannot include more than one tournament per calendar week "
                        f"({week_map[key]!r} and {t.name!r} are in the same week)"
                    ),
                )
            week_map[key] = t.name

    # If a pending playoff config exists, ensure the new schedule has enough
    # eligible future tournaments (scheduled, not the current week's tournament).
    playoff_config = (
        db.query(PlayoffConfig)
        .join(Season, PlayoffConfig.season_id == Season.id)
        .filter(PlayoffConfig.league_id == league.id, Season.is_active.is_(True))
        .first()
    )
    if playoff_config and playoff_config.status == "pending" and playoff_config.playoff_size > 0:
        if playoff_config.playoff_size == 32:
            required = 4
        else:
            required = int(math.log2(playoff_config.playoff_size))

        # Exclude the next upcoming tournament only when no league tournament is in
        # progress. While in progress, picks for the next tournament haven't opened
        # yet, so it remains eligible as a playoff round.
        has_in_progress = any(
            tournament_map[item.tournament_id].status == TournamentStatus.IN_PROGRESS.value
            for item in body.tournaments
            if tournament_map.get(item.tournament_id)
        )

        next_upcoming_id = None
        if not has_in_progress:
            next_upcoming = (
                db.query(Tournament)
                .filter(Tournament.status == TournamentStatus.SCHEDULED.value)
                .order_by(Tournament.start_date.asc())
                .first()
            )
            next_upcoming_id = next_upcoming.id if next_upcoming else None

        eligible = sum(
            1 for item in body.tournaments
            if tournament_map.get(item.tournament_id)
            and tournament_map[item.tournament_id].status == TournamentStatus.SCHEDULED.value
            and (next_upcoming_id is None or tournament_map[item.tournament_id].id != next_upcoming_id)
        )
        if eligible < required:
            raise HTTPException(
                status_code=422,
                detail=(
                    f"Schedule must include at least {required} future tournament(s) for the "
                    f"{playoff_config.playoff_size}-player playoff bracket (found {eligible})"
                ),
            )

    # Atomic replace: delete all existing selections, insert new ones.
    db.query(LeagueTournament).filter_by(league_id=league.id).delete()
    for item in body.tournaments:
        db.add(LeagueTournament(
            league_id=league.id,
            tournament_id=item.tournament_id,
            multiplier=item.multiplier,
        ))
    db.commit()

    # Re-score all completed tournament picks.
    # score_picks uses cached TournamentEntry.earnings_usd — no ESPN API calls.
    for item in body.tournaments:
        tournament = tournament_map.get(item.tournament_id)
        if tournament and tournament.status == TournamentStatus.COMPLETED.value:
            score_picks(db, tournament)

    rows = (
        db.query(LeagueTournament)
        .filter_by(league_id=league.id)
        .join(LeagueTournament.tournament)
        .order_by(Tournament.start_date.asc())
        .all()
    )
    playoff_ids = _playoff_tournament_ids_for_league(league.id, db)
    return [_build_league_tournament_out(row, db, playoff_ids) for row in rows]
